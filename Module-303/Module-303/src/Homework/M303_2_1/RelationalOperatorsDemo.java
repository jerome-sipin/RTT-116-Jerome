package Homework.M303_2_1;

public class RelationalOperatorsDemo {
    public static void main(String[] args) {

        int x = 10, y = 5;
        System.out.println("x > y: " + (x > y));
        System.out.println("x < y: " + (x < y));
        System.out.println("x >= y: " + (x >= y));
        System.out.println("x <= y: " + (x <= y));
        System.out.println("x == y: " + (x == y));
        System.out.println("x != y: " + (x != y));

        int v1 = 50, v2 = 100, v3 = 50;
        System.out.println("variable 1 = " + v1);
        System.out.println("variable 2 = " + v2);
        System.out.println("variable 3 = " + v3);
        System.out.println("variable 1 == variable 2: " + (v1 == v2));
        System.out.println("variable 1 == variable 3: " + (v1 == v3));
    }
}
